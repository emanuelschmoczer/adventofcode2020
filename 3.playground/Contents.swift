var rows = tobogganMap.split(separator: "\n")

func calculateTrees(rows: [String.SubSequence], right: Int, down: Int) -> Int {
    var trees = 0
    var postion = 0
    var i = 0
    while i < rows.count {
        let row = rows[i]
        if "#" == row[row.index(row.startIndex, offsetBy: postion)] {
            trees += 1
        }
        postion += right
        if postion > (row.count - 1) {
            postion = postion - row.count
        }
        i += down
    }
    return trees
}

// Right 1, down 1.
var trees = calculateTrees(rows: rows, right: 1, down: 1)

// Right 3, down 1.
var trees2 = calculateTrees(rows: rows, right: 3, down: 1) // 191

// Right 5, down 1.
var trees3 = calculateTrees(rows: rows, right: 5, down: 1)

// Right 7, down 1.
var trees4 = calculateTrees(rows: rows, right: 7, down: 1)

// Right 1, down 2.
var trees5 = calculateTrees(rows: rows, right: 1, down: 2)

var result = trees * trees2 * trees3 * trees4 * trees5
print(result) // 1478615040
